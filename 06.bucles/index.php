<?php
/* -------------------------------------------------------------------------- */
/*                                 Bucle while                                */
/* -------------------------------------------------------------------------- */
/* Estructura de control que itera o repite la ejecucion de una serie 
de instrucciones tantas veces como sea necesario en base a una condicion. */

/* -------------------------------- Ejemplo 1 ------------------------------- */
$a = 0;
while ($a <= 100) {
    echo $a;
    if ($a != 100) {
        echo ', ';
    }
    $a++;
}

/* -------------------------------- Ejemplo 2 ------------------------------- */
if (isset($_GET['numero'])) {
    $numero = (int)$_GET['numero'];
} else {
    $numero = 1;
}

echo "<h1>Tabla d multiplicar $numero</h1>";
$a = 0;
while ($a <= 10) {
    echo "$a x $numero =" . ($a * $numero) . '<br>';
    $a++;
}

/* -------------------------------------------------------------------------- */
/*                               Bucle Do while                               */
/* -------------------------------------------------------------------------- */

/* -------------------------------- Ejemplo 1 ------------------------------- */
$edad = 17;
$cont = 1;
do {
    echo "Tienes acceso<br>";
    $cont++;
} while ($edad >= 18 && $cont <= 10);

/* -------------------------------------------------------------------------- */
/*                                  Bucle for                                 */
/* -------------------------------------------------------------------------- */

/* -------------------------------- Ejemplo 1 ------------------------------- */
$resultado = 0;
for ($i = 0; $i < 10; $i++) {
    $resultado += $i;
    echo "$resultado<br>";
}

/* -------------------------------------------------------------------------- */
/*                              Instruccion break                             */
/* -------------------------------------------------------------------------- */

for ($i = 0; $i < 10; ++$i) {
    if ($i == 2)
        continue;
    elseif ($i==8) {
        break;
    }
    echo "$i<br>";
}
